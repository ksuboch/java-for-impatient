package ch_01.ex_01;

/**
 * FloorMode
 */
public class FloorMode {
    public static void main(String[] args) {
        System.out.println("Math.floorMod( 5, 2) = " + Math.floorMod( 5, 2));
        System.out.println("Math.floorMod( 4, 2) = " + Math.floorMod( 4, 2));
        System.out.println("Math.floorMod(-4, 2) = " + Math.floorMod(-4, 2));
        System.out.println("Math.floorMod(-5, 2) = " + Math.floorMod(-5, 2));
        System.out.println("Math.floorMod( 5,-2) = " + Math.floorMod( 5,-2));
        System.out.println("Math.floorMod( 4,-2) = " + Math.floorMod( 4,-2));
        System.out.println("Math.floorMod(-4,-2) = " + Math.floorMod(-4,-2));
        System.out.println("Math.floorMod(-5,-2) = " + Math.floorMod(-5,-2));
    }
}